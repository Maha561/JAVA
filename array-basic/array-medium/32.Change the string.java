class Solution {
    String modify(String s) {
        
        
        //StringBuilder result = new StringBuilder();
        
       if(Character.isLowerCase(s.charAt(0))){
           
           return  s.toLowerCase();
       }
       else{
           return s.toUpperCase();
       }
    }
}

